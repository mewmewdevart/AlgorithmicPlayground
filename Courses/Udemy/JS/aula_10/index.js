/*
=	x = y	Atribuição simples
+=	x += y	Soma e atribuição
-=	x -= y	Subtração e atribuição
*=	x *= y	Multiplicação e atribuição
/=	x /= y	Divisão e atribuição
%=	x %= y	Resto da divisão (módulo)
**=	x **= y	Exponenciação e atribuição

*/

let x = 10;

x += 5; // x = x + 5 => x = 15
console.log(x);

x -= 3; // x = x - 3 => x = 12
console.log(x);

x *= 2; // x = x * 2 => x = 24
console.log(x);

x /= 4; // x = x / 4 => x = 6
console.log(x);

x %= 4; // x = x % 4 => x = 2
console.log(x);

x **= 3; // x = x ** 3 => x = 8
console.log(x);
